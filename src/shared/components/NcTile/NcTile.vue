<template>
	<div class="tile" :class="{active: localeActive}" @click="$emit('set-template')">
		<h3>{{ title }}</h3>
		<p>{{ body }}</p>
	</div>
</template>
<script>

export default {
	components: {
	},

	filters: {
		truncate(string, num) {
			if (string.length >= num) {
				return string.substring(0, num) + '...'
			} else {
				return string
			}
		},
	},

	mixins: [],

	props: {
		active: {
			type: Boolean,
			default: false,
		},
		title: {
			type: String,
			default: '',
		},
		body: {
			type: String,
			default: '',
		},
	},

	computed: {
		localeActive: {
			get() {
				return this.active
			},
			set(v) {
				this.$emit('update:active', !!v)
			},
		},
	},

	methods: {
	},

}
</script>
